package uta.cse3310.GamePlay;
/*
 * Class for cord type
 */
public class Cord 
{
	private int x;
	private int y;
	
	public Cord(int x, int y)
	{
		this.x = x;
		this.y = y;
	}

	public int getX()
	{
		return x;
	}

	public int getY()
	{
		return y;
	}

	public void setCord(int x, int y)
	{
		this.x = x;
		this.y = y;
	}

	
}
